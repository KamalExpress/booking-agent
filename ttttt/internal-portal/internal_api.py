from flask import Flask, request, jsonify, render_template_string

app = Flask(__name__)

# Seed 10 unique applicants
applicants = []
first_names = ["SHAHID", "AHMED", "ALI", "FATIMA", "AYESHA", "OMAR", "ZARA", "HASSAN", "SARA", "BILAL"]
surnames = ["RIAZ", "KHAN", "SYED", "TARIQ", "MALIK", "SHEIKH", "CHAUDHRY", "RANA", "IQBAL", "BAIG"]

for i in range(10):
    phone = f"31651859{i:02d}"
    applicants.append({
        "id": i + 1,
        "email": f"applicant{i+1}@example.com",
        "firstname": first_names[i],
        "surname": surnames[i],
        "dob": f"01/01/199{i}",
        "passport": f"FR32145{i}",
        "passport_exp": f"01/01/203{i}",
        "gender_id": "2" if i % 2 == 0 else "1",
        "nationality_id": "197",
        "phone_prefix_id": "197",
        "phone": phone,
        "type": "26",
        "bookingfor": "0",
        "status": "Pending",
        "booking_details": None
    })

# Simulate OTPs generated by the mobile app
otps = {a["phone"]: "123456" for a in applicants}

