@app.route('/get-applicants', methods=['GET'])
def get_applicants():
    # Only return Pending applicants so bots pick up unbooked ones
    pending = [a for a in applicants if a["status"] == "Pending"]
    return jsonify(pending)

@app.route('/update-status', methods=['POST'])
def update_status():
    data = request.json
    applicant_id = data.get("id")
    for a in applicants:
        if a["id"] == applicant_id:
            a["status"] = "Confirmed"
            a["booking_details"] = data.get("slot")
            return jsonify({"success": True}), 200
    return jsonify({"error": "Not found"}), 404

@app.route('/get-otp', methods=['GET'])
def get_otp():
    phone = request.args.get('phone')
    if phone in otps:
        return jsonify({"otp": otps[phone]})
    return jsonify({"error": "No OTP found for this phone number"}), 404

if __name__ == '__main__':
    app.run(port=5001, debug=True)

